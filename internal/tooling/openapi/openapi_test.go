func TestParseHandlersReportsMissingRouter(t *testing.T) {
	dir := t.TempDir()
	writeTestFile(t, filepath.Join(dir, "handler.go"), `package handler

// @Summary Missing router
// @Success 200 {object} dto.UserResponse "OK"
func MissingRouter() {}
`)

	_, err := ParseHandlers(dir, emptyRegistry())
	if err == nil || !strings.Contains(err.Error(), "missing @Router") {
		t.Fatalf("ParseHandlers() error = %v, want missing @Router", err)
	}
}

